    /**
     *
     * @param name:你想要获取的参数名
     * @return 返回一个参数值value
     */
    @Override
    public String getParameter(String name) {
        //先获取路径参数
        String query=this.exchangeRequest.getRequestURI().getRawQuery();
        //判断是否非空
        if (query!=null){
            //非空就解析这个查询字符串
            Map<String ,String > params=getParams(query);
            return params.get(name);
        }

        return null;
    }

    public Map<String, String> getParams(String query) {
        //解析字符串
        //非空判断
        if(query.isEmpty()){
            return Map.of();
        }
        //有字符串
        //分割字符串，将每个参数独立出来
        String []s= Pattern.compile("\\&").split(query);
        Map<String ,String > map=new HashMap<>();
        //遍历字符串数组，解析字符串
        for(String string:s){
            //这样写健壮性强一点 获取第一次出现 = 号的索引
            int num=string.indexOf("=");
            if(num>=1){
                String key=string.substring(0,num);
                String value=string.substring(num+1);
                map.put(key,value);
            }
        }
        return map;
    }

